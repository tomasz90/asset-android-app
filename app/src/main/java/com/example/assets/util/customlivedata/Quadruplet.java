package com.example.assets.util.customlivedata;

public class Quadruplet<S, T, U, W> {

    public S first;
    public T second;
    public U third;
    public W fourth;

    Quadruplet(S first, T second, U third, W fourth) {
        this.first = first;
        this.second = second;
        this.third = third;
        this.fourth = fourth;
    }
}
